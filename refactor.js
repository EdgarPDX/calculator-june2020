//add
export function add(value1, value2) {
    return Number(value1) + Number(value2);
}

//subtract
export function subt(subValue1, subValue2) {
    return Number(subValue1) - Number(subValue2);
}

//divide
export function divider(divValue1, divValue2) {
    return Number(divValue1) / Number(divValue2);
}

//multiply
export function multiplier(mulValue1, mulValue2) {
    return Number(mulValue1) * Number(mulValue2);
}